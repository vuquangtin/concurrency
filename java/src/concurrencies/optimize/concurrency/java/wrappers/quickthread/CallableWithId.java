abstract public class CallableWithId<V, IdType> implements Callable<V> {

	private IdType id;

	public CallableWithId(IdType id) {
		if (id == null)
			throw new IllegalArgumentException("Id can't be null");
		this.id = id;
	}

	public IdType getId() {
		return id;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		CallableWithId<?, ?> other = (CallableWithId<?, ?>) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

}